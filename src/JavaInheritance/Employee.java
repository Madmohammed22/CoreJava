package JavaInheritance;

public class Employee{
    String Name;
    int Age;
    long Number;
    String Address;
    String JobTitle;

    public Employee(String Name, int Age, long Number, String Address,String JobTitle){
        this.Name = Name;
        this.Age = Age;
        this.Number = Number;
        this.Address = Address;
        this.JobTitle = JobTitle;
    }
}
